   <h1>Disiplin Rohani: Melayani <br>~ Pdt. Sapred Pondete</h1>
   <p>26/06/2004 | oleh Henokh Mikhael K.</p>

   <p>Markus 10:42-45 (TB) <br>
   "Tetapi Yesus memanggil mereka lalu berkata: 'Kamu tahu, bahwa mereka yang disebut pemerintah bangsa-bangsa memerintah rakyatnya 
   dengan tangan besi, dan pembesar-pembesarnya menjalankan kuasanya dengan keras atas mereka. Tidaklah demikian di antara kamu. 
   Barangsiapa ingin menjadi besar di antara kamu, hendaklah ia menjadi pelayanmu, dan barangsiapa ingin menjadi yang terkemuka 
   di antara kamu, hendaklah ia menjadi hamba untuk semuanya. Karena Anak Manusia juga datang bukan untuk dilayani, melainkan untuk 
   melayani dan untuk memberikan nyawa-Nya menjadi tebusan bagi banyak orang.'"
   </p>

   <p>Pelayanan Kristen sangat penting untuk pertumbuhan dan transformasi hidup orang percaya agar semakin serupa dengan Kristus. ~ Tony Evans</p>

   <p>Kematian Yesus sebagai teladan pelayanan pengorbanan kepada Tuhan dan orang lain adalah penekanan utama.</p>

   <p>Itulah sebabnya kita harus melatih diri kita untuk melayani, seperti Kristus yang datang untuk melayani bukan untuk dilayani.</p>

   <p>Ketika orang Kristen melatih diri mereka untuk melayani, mereka meneladani Yesus dan menjadi serupa dengan-Nya. ~ Michele Pasley</p>

   <p>Mengapa kita harus melatih diri kita dalam melayani? Ada dua alasan yang diberikan oleh Firman Tuhan (Markus 10:42-52):</p>

   <p><b>1. Melayani merupakan pola hidup dalam kerajaan Allah (ay. 42-43)</b><br>
   Sistem di dalam kerajaan Allah sangat berbeda dengan sistem di dunia ini. Seseorang harus menjadi pelayan terlebih dahulu sebelum Allah mengangkatnya menjadi penguasa. Ini terbukti dalam kehidupan Yusuf, Musa, Yosua, Daud, Timotius, dan bahkan Yesus Kristus sendiri (Filipi 2:1-11) ~ Warren Wiersbe</p>

   <p><b>2. Melayani adalah teladan Kristus yang harus kita ikuti (ay. 45)</b><br>
   "Karena Anak Manusia juga datang bukan untuk dilayani, melainkan untuk melayani dan untuk memberikan nyawa-Nya menjadi tebusan bagi banyak orang." (Markus 10:45 TB)<br>
   Pelayanan harus kita lakukan sebagai murid Kristus, karena Kristus sendiri melakukan ini dan meninggalkan teladan bagi kita untuk mengikuti-Nya. Jika Kristus sang Allah yang Maha Mulia mau menjadi pelayan bagi kita, bagaimana kita tidak mau berbuat lebih sedikit untuk-Nya.</p>

   <p>Filipi 2:5-8 (TB)<br>
   "Hendaklah kamu dalam hidupmu bersama, menaruh pikiran dan perasaan yang terdapat juga dalam Kristus Yesus, yang walaupun dalam rupa Allah, tidak menganggap kesetaraan dengan Allah itu sebagai milik yang harus dipertahankan, melainkan telah mengosongkan diri-Nya sendiri, dan mengambil rupa seorang hamba, dan menjadi sama dengan manusia. Dan dalam keadaan sebagai manusia, Ia telah merendahkan diri-Nya dan taat sampai mati, bahkan sampai mati di kayu salib."</p>

   <p>Tidak ada pelayanan yang terlalu sulit bagi kita, karena Kristus sudah terlebih dahulu memberikan diri-Nya sampai mati di kayu salib untuk melayani kita. ~ Pdt. Sapred Pondete</p>

   <p>Pelayanan Kristen membantu orang percaya untuk bertumbuh dalam iman, hubungan dengan Allah, dan sesama. ~ John Stott</p>
