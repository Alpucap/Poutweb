    <h1>Khawatir</h1>
    <p>26/06/2004 | oleh Henokh Mikhael K.</p>

    <p>"Serahkanlah segala kekuatiranmu kepada-Nya, sebab Ia yang memelihara kamu."<br>
    1 Petrus 5:7 TB</p>

    <p>Sebagai manusia, tentu wajar bisa merasa khawatir. Tapi menjadi hal yang tidak wajar jika kita khawatir berlebihan. 
    Kekhawatiran yang berlebihan dapat mengganggu aktivitas sehari-hari kita. Mungkin yang seharusnya kita bisa 
    melakukan lebih baik, tapi karena terlalu khawatir gagal, kita jadi tidak maksimal dalam menjalankan tugas/tanggung 
    jawab kita.</p>

    <p>Di 1 Petrus 5:7, kita diingatkan untuk menyerahkan segala kekuatiran kepada Tuhan. Artinya, kita berdoa kepada Tuhan 
    mengenai apa yang sedang mengkhawatirkan kita, karena kita yakin bahwa Tuhan yang memelihara kita.</p>

    <p>"Karena itu Aku berkata kepadamu: Janganlah kuatir akan hidupmu, akan apa yang hendak kamu makan atau minum, dan 
    janganlah kuatir pula akan tubuhmu, akan apa yang hendak kamu pakai. Bukankah hidup itu lebih penting dari pada 
    makanan dan tubuh itu lebih penting dari pada pakaian?" <br>
    Matius 6:25 (TB)</p>

    <p>Tuhan Yesus juga mengajar kita dalam Matius 6:25 agar tidak khawatir tentang kebutuhan hidup kita, karena Tuhan akan 
    memelihara kita. Tugas kita adalah melakukan yang terbaik dalam kapasitas kita.</p>

    <p>Yesus menunjukkan pemeliharaan Allah melalui ciptaan-Nya, seperti yang disampaikan dalam Matius 6:26:</p>
    <p>"Pandanglah burung-burung di langit, yang tidak menabur dan tidak menuai dan tidak mengumpulkan bekal dalam lumbung, 
    namun diberi makan oleh Bapamu yang di sorga. Bukankah kamu jauh melebihi burung-burung itu?" (TB)</p>

    <p>Kebenaran ini mengajarkan kita untuk melihat ke dalam diri sendiri, mengevaluasi apakah kekhawatiran mengikat pikiran 
    kita.</p>

    <p>Filipi 4:6 mengingatkan kita:</p>
    <p>"Janganlah hendaknya kamu kuatir tentang apa pun juga, tetapi nyatakanlah dalam segala hal keinginanmu kepada Allah 
    dalam doa dan permohonan dengan ucapan syukur." (TB)</p>

    <p>Ketika kita merasa khawatir, kita dapat datang kepada Tuhan melalui doa, karena itu merupakan hak istimewa kita untuk 
    berkomunikasi dengan Allah, sebagaimana disampaikan dalam Filipi 4:6.</p>
