    <h1>Doa</h1>
    <p>26/06/2004 | oleh Henokh Mikhael K.</p>
    <p>"Bertekunlah dalam doa dan dalam pada itu berjaga-jagalah sambil mengucap syukur." <br>
        Kolose 4:2 TB
    </p>

    <p>Sebagai orang percaya, kita memiliki hak istimewa untuk bersekutu dan berbicara dengan Tuhan melalui doa. 
        Namun terkadang kita seringkali menganggap remeh pentingnya berdoa. Dalam ayat ini kita kembali diingatkan untuk 
        bertekun dalam doa.</p>
        
    <p>Doa merupakan suatu hal yang penting bagi orang percaya karena dikatakan doa seperti nafas orang percaya. 
        Maka ketika kita berhenti berdoa, kerohanian kita bisa dikatakan mati.</p>

    <p>Kita harus melatih diri kita untuk berdoa dan berjaga-jaga. Sebagai manusia kita terkadang terlena dengan 
        sifat kemanusiaan kita, yang akhirnya melalaikan kehidupan doa kita. Atau bahkan ketika berdoa kita memikirkan hal lain.
        Dalam hal ini kita memerlukan kedisiplinan rohani untuk fokus ketika berdoa, untuk mencapai tujuan dari doa kita dan tidak 
        mengalihkan pikiran kita pada saat kita berdoa.</p>

    <p>Kita harus melakukannya sambil mengucap syukur, artinya kita tidak hanya berdoa untuk kepentingan kita semata. 
        Seringkali kita berdoa kepada Tuhan, tapi fokus doa kita hanya untuk kepentingan diri kita atau fokusnya meminta. 
        Sedangkan apa yang diajarkan dalam ayat ini adalah kita harus juga selalu mengucap syukur dalam doa kita, 
        sebab oleh karena Tuhan saja kita bisa menerima dan mendapatkan apa yang kita miliki/nikmati saat ini.</p>

    <p>Doa bukan hanya meminta, tetapi juga bersyukur atas apa yang telah kita terima dari Allah</p>
    <p>"Jika satu-satunya doa yang pernah anda ucapkan dalam hidup anda adalah mengucap syukur, itu cukup."</p>
